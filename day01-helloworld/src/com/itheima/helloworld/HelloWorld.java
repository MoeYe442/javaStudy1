package com.itheima.helloworld;

public class HelloWorld {
    static void main() {
        System.out.println("Hello World!");
        printTen();
    }
    //打印十行Hello World
    static void printTen() {
        for (int i = 0; i < 10; i++) {
            System.out.println("Hello World!");
        }
    }

}
